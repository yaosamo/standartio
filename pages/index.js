import Head from "next/head";
import styles from "../styles/Home.module.css";
import IconsList from "../components/iconslist";
import IconsData from "../components/icons.json";
import React, { useState } from "react";

export default function Home() {
  const [checked, setChecked] = useState(false);

  const handleSizeChange = () => {
    setChecked(!checked);
  };

  const [searchField, setSearchField] = useState("");
  const IconsSorted = []
    .concat(IconsData)
    .sort((a, b) => (a.name > b.name ? 1 : -1));
  const filteredIcons = IconsSorted.filter((icon) => {
    return icon.tags.toLowerCase().includes(searchField.toLowerCase());
  });

  const handleSearchChange = (e) => {
    setSearchField(e.target.value);
  };

  return (
    <div className={styles.container}>
      <Head>
        <title>Standartio</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>
        {/* Icons List */}
        <IconsList filteredIcons={filteredIcons} ratio={checked ? 2 : 1} />
        {/* Search Field */}
        <div className={styles.search}>
          <input
            className={styles.search_input}
            jsaction="paste:puy29d;"
            maxLength="2048"
            name="q"
            type="text"
            aria-autocomplete="both"
            aria-haspopup="false"
            autoCapitalize="off"
            autoComplete="off"
            autoCorrect="off"
            autoFocus=""
            role="combobox"
            spellCheck="false"
            title="Search"
            placeholder="Search"
            aria-label="Search"
            onChange={handleSearchChange}
          />
        </div>
        <label className={styles.setting_size}>
          <input type="checkbox" onChange={handleSizeChange} />
          <span className={styles.indicator}>x2</span>
        </label>
      </main>
    </div>
  );
}
